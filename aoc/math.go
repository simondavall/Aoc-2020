package aoc

func Max(a int, b int) int {
	if a > b {
		return a
	} else {
		return b
	}
}

func Min(a int, b int) int {
	if a < b {
		return a
	} else {
		return b
	}
}

func Max64(a int64, b int64) int64 {
	if a > b {
		return a
	} else {
		return b
	}
}

func Min64(a int64, b int64) int64 {
	if a < b {
		return a
	} else {
		return b
	}
}
